# Функция для преобразования числа в словесную форму
def number_to_words(n):
    # Одиничные числа и ноль
    units = ["ноль", "один", "два", "три", "четыре", "пять", "шесть", "семь", "восемь", "девять"]
    # Числа от 10 до 19
    teens = ["десять", "одиннадцать", "двенадцать", "тринадцать", "четырнадцать", "пятнадцать", 
             "шестнадцать", "семнадцать", "восемнадцать", "девятнадцать"]
    # Десятки
    tens = ["", "", "двадцать", "тридцать", "сорок", "пятьдесят", 
            "шестьдесят", "семьдесят", "восемьдесят", "девяносто"]
    
    if n < 10:
        # В случае, если число меньше 10
        return units[n]
    elif n < 20:
        # Для чисел от 10 до 19
        return teens[n-10]
    else:
        # Для чисел от 20 до 99
        return tens[n // 10] + (' ' + units[n % 10] if n % 10 != 0 else '')
   
def main():
    while True:
        # Запрос ввода от пользователя
        user_input = input("Введите число от 0 до 99: ")
        try:
            # Пытаемся преобразовать введенное значение в целое число
            number = int(user_input)
            if 0 <= number <= 99:
                # Если число в нужном диапазоне, выводим его текстовое представление
                print(number_to_words(number))
                break  # Прерываем цикл после успешного вывода
            else:
                # Значение не в диапазоне от 0 до 99
                print("Число должно быть в диапазоне от 0 до 99!")
        except ValueError:
            # В случае ошибки преобразования ввода в число
            print("Вы должны ввести число!")

# Вызов главной функции
main()